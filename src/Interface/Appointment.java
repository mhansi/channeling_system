    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String did = txt_did.getText();
     
         SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(jDateChooser1.getDate());
     
     
     
        try {
            ResultSet resultset = Get_Connection.connecion().executeQuery("select count(Number) as x from appointment where Doctor_ID='"+did+"' AND Date='"+date+"';");
            
            if (resultset.next()) {
                int rowcount = resultset.getInt("x");
                rowcount++;
              
                jLabel8.setText("" + rowcount);
                
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
   
    }//GEN-LAST:event_jButton1ActionPerformed

