    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String id = txt_id.getText();
        
        try {
            ResultSet resultset = Get_Connection.connecion().executeQuery("select * from appointment where Appointment_ID = '" +id+ "'");
            if (resultset.next()) {
                txt_rid.setText(resultset.getString("Reception_ID"));
                txt_pid.setText(resultset.getString("Patient_ID"));
                jDateChooser1.setDate(resultset.getDate("Date"));
                jLabel8.setText(resultset.getString("Number"));
                txt_did.setText(resultset.getString("Doctor_ID"));
                
                
            } else {
                JOptionPane.showConfirmDialog(this, "Unknown ID");
                txt_id.setText(null);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void txt_ridFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_ridFocusLost
       String id = txt_rid.getText();
        try {
            ResultSet resultset = Get_Connection.connecion().executeQuery("select * from receptient where Receptient_ID= '" +id+ "'");
            
            if (resultset.next()) {
                String status = resultset.getString("Status");
                if(status.equals("N")){
                   JOptionPane.showMessageDialog(this, "Not an active member");
                   txt_rid.setBackground(Color.red);
                   txt_rid.setText("");
                }}else{
            JOptionPane.showMessageDialog(this, "Invalid Receptient Number");
            txt_rid.setText("");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txt_ridFocusLost

